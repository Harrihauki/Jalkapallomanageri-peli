    private void pelaajanPoisto(Muodostelma muodostelma, Pelaaja pelaaja) {

        if (muodostelma.getMaalivahti().equals(pelaaja)) {
            muodostelma.poistaMaalivahti();
        } else {
            muodostelma.getAvauskentallinen().remove(pelaaja);
        }
    }

